package com.company;

public class CalculatorObject {

    //add int together
    public int add(int x, int y)    {
        return x + y;
    }
    //add double together
    public double addDouble(double x, double y) {
        return x + y;
    }
    //subtract int together
    public int subtract(int x, int y)   {
        return x - y;
    }
    //subtract double together
    public double subtractDouble(double x, double y)  {
        return x - y;
    }
    //multiply int together
    public int multiply(int x, int y)   {
        return x * y;
    }
    //multiply double together
    public double multiplyDouble(double x, double y)  {
        return x * y;
    }
    //divide int together
    public int divide(int x, int y) {
        return x / y;
    }
    //divide double together
    public double divideDouble(double x, double y)    {
        return x / y;
    }
}
